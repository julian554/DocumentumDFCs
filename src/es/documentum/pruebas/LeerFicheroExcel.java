    private void opcionExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opcionExcelActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new java.io.File("/"));
        chooser.setDialogTitle("Seleccionar nombre de fichero");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Ficheros Excel", "xls", "xlsx");
        chooser.setFileFilter(filter);
        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            String fichero = chooser.getSelectedFile().toString();
            ImportarExcelEnTabla(fichero);

        }
    }//GEN-LAST:event_opcionExcelActionPerformed

    public void ImportarExcelEnTabla(String fichero) {
        Utilidades util = new Utilidades();
        int numHojas = util.numeroHojasExcel(fichero);
        Path p = Paths.get(fichero);
        String nombreFichero = p.getFileName().toString();

        this.setTitle("Visor de ficheros Excel - " + fichero);

        int numtab = panelTabExcel.getTabCount();
        for (int t = numtab - 1; t > 0; t--) {
            panelTabExcel.remove(t);
        }
        panelTabExcel.setTitleAt(0, "Hoja - 1");
        TableModel modelo = util.importarExcelEnTableModel(fichero, 0);
        tablaExcel.setModel(modelo);
        tablaExcel.setAutoscrolls(true);
        resizeColumnWidth(tablaExcel);
        tablaExcel.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tablaExcel.setShowHorizontalLines(true);
        tablaExcel.setShowVerticalLines(true);

        if (numHojas > 1) {
            scroll = new JScrollPane[numHojas];
            tabla = new JTable[numHojas];

            for (int n = 1; n < numHojas; n++) {
                modelo = util.importarExcelEnTableModel(fichero, n);
//                final MouseListener[] mouselistener = new MouseListener[numHojas];
                if (modelo.getRowCount() > 0) {
                    tabla[n] = new JTable();
                    scroll[n] = new JScrollPane();
                    scroll[n].add(tabla[n]);
                    tabla[n].setName("HOJA - " + (n + 1));
                    tabla[n].setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                    tabla[n].setShowHorizontalLines(true);
                    tabla[n].setShowVerticalLines(true);
                    scroll[n].setAutoscrolls(true);
                    tabla[n].setAutoCreateRowSorter(true);
                    tabla[n].setColumnSelectionAllowed(true);
                    scroll[n].setViewportView(tabla[n]);
                    tabla[n].getColumnModel().getSelectionModel().setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
                    tabla[n].setModel(modelo);
                    final int posicion = n;
                    final MouseListener[] mouselistener = new MouseListener[n+1];
                    tabla[n].addMouseListener(new java.awt.event.MouseAdapter() {
                        public void mousePressed(java.awt.event.MouseEvent evt) {
                            tablaMousePressed(evt);
                        }

                        public void tablaMousePressed(MouseEvent evt) {
                            if (evt.getButton() == java.awt.event.MouseEvent.BUTTON3) {
                                botonderecho = true;
                                componente = "HOJA" + posicion;
                                evt.getComponent().addMouseListener(mouselistener[posicion]);
                                popupmenu(evt);
                            }
                        }
                    });
                    panelTabExcel.addTab(nombreFichero + n + 1, scroll[n]);
                    panelTabExcel.setTitleAt(n, "Hoja - " + (n + 1));
                    resizeColumnWidth(tabla[n]);
                }

            }
        }

    }

    public void resizeColumnWidth(JTable table) {
        final TableColumnModel columnModel = table.getColumnModel();
        for (int column = 0; column < table.getColumnCount(); column++) {
            int width = 180; // Min width
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer renderer = table.getCellRenderer(row, column);
                Component comp = table.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width + 1, width);
            }
            if (width > 600) {
                width = 600;
            }
            columnModel.getColumn(column).setPreferredWidth(width);
        }
    }

